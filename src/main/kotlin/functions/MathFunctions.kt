package functions

import kotlin.math.*

fun mathFunctions() {
    println("Max between 10 and 5 is ${max(5, 10)}")

    println("Min between 10 and 5 is ${min(5, 10)}")

    println("Square root 45 = ${sqrt(45f)}, Square root 81 = ${sqrt(81.0)}")

    println("PI = $PI")

    println("Euler = $E")

    println("Round ${round(PI)}")
}